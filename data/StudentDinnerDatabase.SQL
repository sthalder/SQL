DinnerID INT IDENTITY PRIMARY KEY,
SID INT NOT NULL FOREIGN KEY REFERENCES dbo.Student (SID),
RID INT NOT NULL FOREIGN KEY REFERENCES dbo.Restaurant (RID),
DinnerDay VARCHAR (10),
Cost DECIMAL,
Rating INT
);


